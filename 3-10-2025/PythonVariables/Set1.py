# Creating elements in Set in Python

name_set = {"Ramu","chomu","promu","dimu"}
print(name_set)

number_set = {10,20,30,40,50}
print(number_set)

boolean_set = {True,False,True,False}
print(boolean_set)

myset_num1 = {10,"Amit","Set2",True,False}
print(myset_num1)

for element in myset_num1:
    print(element)