# Slicing in Python
#+ve index starts from left side and it starts from 0
dataEng = "Azure Data Field is one of the Niche Technology to Learn in AI Era.."
print("In Demand Tech: ",dataEng[0:5])

#Slicing by using negative index
#-ve Index starts from the right Side and it  starts from -1
print("Slicing by using -ve Index: ",dataEng[-8:-2])
